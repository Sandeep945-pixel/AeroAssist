# This is our LLM "attacker"
def get_llm_guess(prompt_text):
    """
    Sends a prompt to the Gemini API and parses the JSON response.
    """
    try:
        model = genai.GenerativeModel('gemini-2.5-pro')
        response = model.generate_content(
            prompt_text,
            generation_config=genai.types.GenerationConfig(
                temperature=0.0,  # We want deterministic, factual answers
                response_mime_type="application/json"
            )
        )
        # Give a short delay to avoid hitting API rate limits
        time.sleep(1)
        return json.loads(response.text)
    except Exception as e:
        print(f"  !! API Error: {e}")
        # Return a structured error
        return {
            "guess": "[API Error]",
            "confidence_level": "N/A",
            "reasoning": str(e)
        }

# This function generalizes the age, which is a common k-anonymity step
