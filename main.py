def upload_and_wait_for_file(video_filepath):
    """Uploads a video file to the Gemini API and waits for it to be active."""
    if not os.path.exists(video_filepath):
        raise FileNotFoundError(f"Error: The video file '{video_filepath}' was not found.")

    print(f"\n--- Uploading '{video_filepath}'... ---")
    video_file = genai.upload_file(path=video_filepath)
    print(f"Upload complete. File Name: {video_file.name}. Now processing...")

    while video_file.state.name == "PROCESSING":
        print("File is still processing, waiting 10 seconds...")
        time.sleep(10)
        video_file = genai.get_file(name=video_file.name)

    if video_file.state.name == "FAILED":
        raise ValueError(f"File processing failed for {video_file.name}.")

    print(f"File '{video_file.name}' is now ACTIVE and ready for analysis.")
    return video_file


