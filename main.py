# 1. We want to ask a query: "What is the average age of musicians?"
musician_ages = df[df['Category'] == 'Musician']['Age'].values
true_average_age = musician_ages.mean()

print(f"  Query: 'What is the average age of musicians in the dataset?'")
print(f"  > Ground Truth Answer: {true_average_age:.2f}")

# 2. Now, we ask the *same query* using a DP-protected mechanism.
#    Epsilon (ε) is the "privacy budget". Lower = more private (more noise).
epsilon = 1.0

# We must define the 'bounds' (min/max age) for the DP algorithm
age_bounds = (20, 90)

dp_average_age = dpt.mean(musician_ages, epsilon=epsilon, bounds=age_bounds)

print(f"  > DP-Protected Answer (ε={epsilon}): {dp_average_age:.2f}")

# 3. Show what happens when you run it multiple times
print("  > Running the DP query 3 more times to show noise:")
for i in range(3):
    print(f"    - DP Run {i+1}: {dpt.mean(musician_ages, epsilon=epsilon, bounds=age_bounds):.2f}")

